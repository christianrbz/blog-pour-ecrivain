<h2>Jean Forteroche</h2><br>
<p>
Né le 16 juillet 1974 à Paris. Ecrivain français depuis une vingtaine d'année, ses livres se basent sur ses différentes expériences de voyage à travers le monde.
</p>

<h3>Biographie</h3>
<p>Enfant, Jean Forteroche découvra rapidement la littérature grâce à la riche bibliothèque de son père. Il y'avait des livres de tous les genres, et les dévorait une par une. 
A l'âge de 11 ans, son professeur de français organisa un concours de nouvelles. A la tâche, Jean prit énormément de plaisirs à rédiger sa nouvelle, et gagna facilement le concours. C'est depuis ce jour que Jean ne cessa plus d'écrire. Il avait son propre carnet et écrivait toutes ces idées, ses rêves et ses expériences. <br><br>

A 18ans, il obtient son baccalauréat et n'ayant pas les moyens pour faire des études, il trouva un travail de facteur. <br><br>

A 23 ans, Jean se lassait de sa routine et décida de partir très loin de Paris pour découvrir le monde. Au début, il voulait découvrir l'Italie. Cette idée de vouloir voyager a été pour lui une révélation. Il avait plein d'idées, de nouvelles histoires à écrire dans son fameux carnet. Il avait enfin l'impression de vivre une nouvelle vie.
Il décida d'écrire, et avec le soutien de ses amis, de publier un roman qui tracerait son premier voyage. Son premier roman fut un large succès et décida de voyager à travers le monde pour écrire d'autres romans.</p>

